#define fast ios_base::sync_with_stdio(false);cin.tie(nullptr);cout.tie(nullptr);
#define ll long long
#define endl '\n'

void solve() {
    int n, k;
    cin >> n >> k;
    ll arr[n * k];
    for (auto &i : arr) {
        cin >> i;
    }
    ll sum = 0;
    for (int i = (n * k) - (n / 2) - 1; k--; i = i - (n / 2) - 1) {
        sum += arr[i];
    }
    cout << sum << endl;
}

