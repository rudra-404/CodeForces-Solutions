void solve() {
    ll n, k, x;
    cin >> n >> k >> x;
    ll min_sum = (k * (k + 1)) / 2;
    ll max_sum = (k * ((2 * n) + 1 - k)) / 2;
    if (x >= min_sum && x <= max_sum) cout << "YES" << endl;
    else cout << "NO" << endl;
}

