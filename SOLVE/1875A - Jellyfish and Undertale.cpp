#include<bits/stdc++.h>
using namespace std;
#define fast ios_base::sync_with_stdio(false);cin.tie(nullptr);cout.tie(nullptr);
#define ll long long
#define endl '\n'

void solve() {
    ll a, b, n;
    cin >> a >> b >> n;
    ll arr[n];
    for (auto &i:arr) {
        cin >> i;
    }
    ll timer = b - 1;
    b = 1;
    for (ll i = 0; i < n; i++) {
        b = min(b + arr[i], a);
        timer += (b - 1);
        b = 1;
    }
    cout << timer + b << endl;
}

