void solve() {
    int n;
    cin >> n;
    if (n == 1) {
        cout << 1 << endl;
        return;
    }
    if (n % 2 == 0) {
        cout << -1 << endl;
        return;
    }
    for (int i = 0; i < n; ++i) {
        cout << (i * 2) % n + 1 << " ";
    }
    cout << endl;
}

