#define fast ios_base::sync_with_stdio(false);cin.tie(nullptr);cout.tie(nullptr);
#define ll long long
#define endl '\n'

void solve() {
    int n, k;
    cin >> n >> k;
    vector<int> all(k);
    for (int i = 0; i < k; i++) {
        int a, b;
        cin >> a >> b;
        all[a - 1] += b;
    }
    sort(all.rbegin(), all.rend());
    cout << accumulate(all.begin(), all.begin() + min(n, k), 0) << endl;
}

