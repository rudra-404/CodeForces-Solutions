#include<bits/stdc++.h>
using namespace std;
#define fast ios_base::sync_with_stdio(false);cin.tie(nullptr);cout.tie(nullptr);
#define ll long long
#define endl '\n'

void solve() {
    int n;
    cin >> n;
    ll arr[n + 1];
    arr[0] = 0;
    for (int i = 1; i < n + 1; i++) {
        cin >> arr[i];
    }
    int op = 0;
    for (int i = 1; i < n + 1; i++) {
        if (arr[i - 1] == 0 && arr[i] != 0) {
            op++;
        }
    }
    cout << (op > 2 ? 2 : op) << endl;
}

