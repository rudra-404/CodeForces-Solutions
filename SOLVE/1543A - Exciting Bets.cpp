#include<bits/stdc++.h>
using namespace std;
#define fast ios_base::sync_with_stdio(false);cin.tie(nullptr);cout.tie(nullptr);
#define ll long long
#define endl '\n'

void solve() {
    ll a, b;
    cin >> a >> b;
    if (a == b) {
        cout << "0 0" << endl;
        return;
    }
    ll difference = abs(a - b);
    ll x = a % difference;
    ll y = b % difference;
    if (x != y) {
        cout << "0 0" << endl;
        return;
    }
    cout << difference << ' ' << min(x, abs(difference - x)) << endl;
}

int main() {
    fast;
    ll test;
    cin >> test;
    while (test--) solve();
    return 0;
}